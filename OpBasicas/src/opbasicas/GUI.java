    private void btnOkLenguajeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOkLenguajeActionPerformed
        String alfabeto = txtAlfabeto.getText();
        if(alfabeto.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Escribe un alfabeto valido.");
        } else {
            Lenguaje lenguaje = new Lenguaje(alfabeto);
            int longMax = (int) spnLongMax.getValue();
            if(longMax < 1) {
                JOptionPane.showMessageDialog(null, "Selecciona una longitud valida.");
            } else {
                Set<String> kleene = lenguaje.obtenerKleene(longMax);
                Set<String> positiva = lenguaje.obtenerPositiva(longMax);
                
                Set<String> kle = new TreeSet<>(kleene);
                Set<String> pos = new TreeSet<>(positiva);
                
                areaKleene.setText(kle.toString());
                areaPositiva.setText(pos.toString());
            }
        }
        
    }//GEN-LAST:event_btnOkLenguajeActionPerformed

    private void btnSaveLenguajeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveLenguajeActionPerformed
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setDialogTitle("Guardar resultados");

        int seleccion = fileChooser.showSaveDialog(this);

        if (seleccion == JFileChooser.APPROVE_OPTION) {

            File archivo = fileChooser.getSelectedFile();

            if (!archivo.getName().toLowerCase().endsWith(".txt")) {
                archivo = new File(archivo.getAbsolutePath() + ".txt");
            }

            try (BufferedWriter writer = new BufferedWriter(new FileWriter(archivo))) {

                writer.write("RESULTADOS DE LA PRACTICA DE LENGUAJES\n");
                writer.write("=======================================\n\n");

                writer.write("CERRADURA POSITIVA (L+)\n");
                writer.write(areaPositiva.getText());
                writer.write("\n\n");

                writer.write("CERRADURA DE KLEENE (L*)\n");
                writer.write(areaKleene.getText());

                JOptionPane.showMessageDialog(this,
                        "Archivo guardado correctamente.");

            } catch (IOException e) {
                JOptionPane.showMessageDialog(this,
                        "Error al guardar el archivo.",
                        "Error",
                        JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnSaveLenguajeActionPerformed

