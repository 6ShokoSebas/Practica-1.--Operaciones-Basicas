    private void btnOkCadenasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOkCadenasActionPerformed
        if(txtCadena.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Escribe una cadena valida.");
        } else {
            String cadena = txtCadena.getText();
            
            Set<String> subcadenas = Lenguaje.obtenerSubcadenas(cadena);
            Set<String> prefijos = Lenguaje.obtenerPrefijos(cadena);
            Set<String> sufijos = Lenguaje.obtenerSufijos(cadena);
            
            Set<String> pref = new TreeSet<>(prefijos);
            Set<String> subc = new TreeSet<>(subcadenas);
            Set<String> sufi = new TreeSet<>(sufijos);
            
            pref.remove("");
            subc.remove("");
            sufi.remove("");
            
            pref.add(" Ø");
            subc.add(" Ø");
            sufi.add(" Ø");
            
            areaSubcadenas.setText(subc.toString());
            areaPrefijos.setText(pref.toString());
            areaSufijos.setText(sufi.toString());
        }
    }//GEN-LAST:event_btnOkCadenasActionPerformed

